// What is Docker?

// Docker is a platform that allows developers to package applications along with all their 
// dependencies into lightweight, portable containers. These containers run the same way on any system, 
// solving the classic ‘works on my machine’ problem.


// A Docker container includes everything the app needs — code, libraries, system tools — and runs 
// isolated from the host system. 


// ** Mention Real-World Use Case
// For example, in a React + Node.js project, Docker can package the frontend and backend together with the 
// exact Node version and dependencies. Then, the app runs the same way on any developer’s machine or in 
// production without manual setup.

